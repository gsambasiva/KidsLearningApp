export const LESSONS = { ...mathLessons, ...readingLessons };

/**
 * Get lesson content for a given topic.
 * Falls back to a generic lesson if topic not found.
 */
export function getLessonForTopic(topic) {
  if (!topic) return null;
  return LESSONS[topic] || LESSONS['counting'] || null;
}

/**
 * Get all lessons for a subject.
 */
export function getLessonsForSubject(subject) {
  return Object.values(LESSONS).filter((l) => l.subject === subject);
}

/**
 * Get lesson color (for theming the lesson screen).
 */
export function getLessonColor(topic) {
  return LESSONS[topic]?.color || '#3F51B5';
}

export default LESSONS;
